#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		s1s;

	i = 0;
	s1s = 0;
	while (s1[s1s])
		s1s++;
	while (i < n && s2[i])
		s1[s1s++] = s2[i++];
	s1[s1s] = '\0';
	return (s1);
}
